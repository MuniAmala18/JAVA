package com.kodnest.patterns.level4;

public class Pattern2 {
	public static void main(String[] args) {
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=5;j++) {
				System.out.print("#"); //Print #
			}
			for(int k=1;k<=5;k++) {
				System.out.print("*"); //print *
			}
			System.out.println();
		}

	}
}
